# String Functions
# len() -> return no of chars
print(len(course))

# [index] -> get character at index
print(course[5])
print(course[-1])  # return last character

# [start:end] -> slice string
print(course[2:12])
print(course[0:3])
print(course[0:])  # goes to last by default
print(course[:3])  # starts from 0 by default
print(course[:])
